    @Override
    protected final void validaAcessoUsuario()
    {
        String nivelAcesso = GerenciadorNivelAcesso.obterInstancia().getNivelAcesso();

        if (nivelAcesso.equals(NivelAcesso.Comum.name()))
        {
            this.dispose();
            abrirPaginaLogin();
        }
    }

    private void PreencherCampoNivelAcesso()
    {
        try
        {
            List<TabelaGeralDependenteDTO> listaPerfilDto = CadastraUsuarioValidation.PreencherCampoNivelAcesso();

            listaPerfilDto.forEach((perfil) ->
            {
                cbNivelAcesso.addItem(perfil.getDescricao());
            });
        }
        catch (Exception ex)
        {
            MensagemAlertaErro("Erro ao preencher o a lista de acesso", true);
        }
    }

    private void PreencherCep(String cep)
    {
        String[] array = BuscaAutomaticaCep.buscarCep(cep);
        String logradouro = array[7];
        String bairro = array[15];
        String cidade = array[19];
        String uf = array[23];

        txtRua.setText(logradouro);
        txtBairro.setText(bairro);
        txtCidade.setText(cidade);
        txtEstado.setText(uf);
    }

    private void MensagemAlertaErro(String mensagem, boolean erro)
    {
        Color cor = erro ? Color.red : Color.blue;

        lblMensagemErroCadastroUsuario.setVisible(true);
        lblMensagemErroCadastroUsuario.setForeground(cor);
        lblMensagemErroCadastroUsuario.setText(mensagem);
    }

    private UsuarioDTO getUsuarioDto() throws NumberFormatException
    {
        // TODO add your handling code here:
        String nome = txtNome.getText();
        long cpf = txtCpf.getValue() == null ? -1 : Long.parseLong(txtCpf.getValue().toString().trim());
        String cep = txtCep.getText().trim();
        String rua = txtRua.getText().trim();
        String bairro = txtBairro.getText().trim();
        String cidade = txtCidade.getText().trim();
        String estado = txtEstado.getText().trim();
        String nivelAcesso = (String) cbNivelAcesso.getSelectedItem();

        UsuarioDTO usuarioDto = new UsuarioDTO();
        usuarioDto.setNome(nome);
        usuarioDto.setCPF(cpf);
        usuarioDto.getEndereco().setCEP(cep);
        usuarioDto.getEndereco().setRua(rua);
        usuarioDto.getEndereco().setBairro(bairro);
        usuarioDto.getEndereco().setCidade(cidade);
        usuarioDto.getEndereco().setEstado(estado);
        usuarioDto.getPerfil().setDescricao(nivelAcesso);
        usuarioDto.setSenha("123456");

        return usuarioDto;
    }
