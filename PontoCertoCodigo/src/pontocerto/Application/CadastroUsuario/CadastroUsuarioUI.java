    private void btnSalvarUsuarioMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_btnSalvarUsuarioMouseClicked
    {//GEN-HEADEREND:event_btnSalvarUsuarioMouseClicked
        try
        {
            UsuarioDTO usuarioDto = getUsuarioDto();

            if (CadastraUsuarioValidation.validaCampos(usuarioDto))
            {
                MensagemAlertaErro("Preencha os campos obrigatórios", true);
            }
            else if (CadastraUsuarioValidation.validaUsuarioExistente(usuarioDto))
            {
                MensagemAlertaErro("Usuário já existe", true);
            }
            else if (!CadastraUsuarioValidation.validaCPF(Long.toString(usuarioDto.getCPF())))
            {
                MensagemAlertaErro("CPF inválido", true);
            }
            else if (CadastraUsuarioValidation.validaUsuarioEndereco(usuarioDto))
            {
                MensagemAlertaErro("Usuário já cadastrado para o endereço informado", true);
            }
            else
            {
                CadastraUsuarioValidation.cadastraUsuario(usuarioDto);
                MensagemAlertaErro("Usuário cadastrado com sucesso", false);
            }
        }
        catch (Exception ex)
        {
            MensagemAlertaErro("Ocorreu um erro ao cadastrar um usuário", true);
        }
    }//GEN-LAST:event_btnSalvarUsuarioMouseClicked

    private void txtCepFocusLost(java.awt.event.FocusEvent evt)//GEN-FIRST:event_txtCepFocusLost
    {//GEN-HEADEREND:event_txtCepFocusLost
        // TODO add your handling code here:
        try
        {
            String cep = txtCep.getText().trim();

            if (cep != null)
            {
                Integer.parseInt(cep);
                PreencherCep(cep);
            }
        }
        catch (NumberFormatException ex)
        {
            MensagemAlertaErro("CEP no formato incorreto", true);
        }
    }//GEN-LAST:event_txtCepFocusLost

