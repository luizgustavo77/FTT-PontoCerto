    @Override
    protected void validaAcessoUsuario()
    {
        String nivelAcesso = GerenciadorNivelAcesso.obterInstancia().getNivelAcesso();

        if (nivelAcesso.equals(NivelAcesso.Comum.name()))
        {
            lblUsuario.setVisible(false);
            btnNovoItinerario.setVisible(false);
            btnDeletaItinerario.setVisible(false);
        }
    }

    private void instanciaTableModel()
    {
        model = (DefaultTableModel) tbConsultaItinerario.getModel();
    }

    private void PreencherGrid()
    {
        try
        {
            List<RotasDTO> listaRota = ConsultaRotaValidation.buscarRotaPorFretado(idFretado);
            model.setRowCount(0);

            if (listaRota.size() != 0)
            {
                listaRota.forEach((item) ->
                {
                    model.insertRow(model.getRowCount(), new Object[]
                    {
                        item.getFretado().getNumero(), item.getParada().getCEP(), item.getParada().getRua(), item.getParada().getBairro(), item.getParada().getCidade(), item.getParada().getEstado()
                    });
                });
            }
            else
            {
                MensagemAlertaErro("Nenhuma rota encontrada para o fretado selecionado", true);
            }
        }
        catch (Exception ex)
        {
            MensagemAlertaErro("Erro ao preencher a tabela com as rotas", true);
        }
    }

    private void MensagemAlertaErro(String mensagem, boolean erro)
    {
        Color cor = erro ? Color.red : Color.blue;

        lblMensagemErroConsultaItinerario.setVisible(true);
        lblMensagemErroConsultaItinerario.setForeground(cor);
        lblMensagemErroConsultaItinerario.setText(mensagem);
    }
