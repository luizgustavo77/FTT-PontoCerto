    private void btnDeletaItinerarioMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_btnDeletaItinerarioMouseClicked
    {//GEN-HEADEREND:event_btnDeletaItinerarioMouseClicked
        // TODO add your handling code here:
        try
        {
            if (tbConsultaItinerario.getRowCount() != 0 && tbConsultaItinerario.getSelectedRow() != -1)
            {
                String cep = model.getValueAt(tbConsultaItinerario.getSelectedRow(), 1).toString();
                
                RotasDTO rota = new RotasDTO();
                rota.getFretado().setId(idFretado);
                rota.getParada().setCEP(cep);
                
                CadastroItinerarioValidation.excluiRota(rota);

                PreencherGrid();

                MensagemAlertaErro("Usuário excluído com sucesso", false);
            }
            else
            {
                MensagemAlertaErro("Nenhum usuário selecionado", true);
            }
        }
        catch (Exception ex)
        {
            MensagemAlertaErro("Ocorreu um erro ao deletar uma rota", true);
        }
    }//GEN-LAST:event_btnDeletaItinerarioMouseClicked

